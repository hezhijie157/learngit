#include "config.h"
#include "Gpio.h"

#define LED_Port  _ph							
#define LED_PortC _phc

void Gpio_Init(void)
{
	LED_PortC=0x0;									//O定 LED_Port 檩出模式
	LED_Port=0xFE;									//O定 LED_Port 初值
	
	
}

static void DelayUs(unsigned int us)
{	
	while(us--);
}

void Gpio_Operate(void)
{
	LED_Port = 0x00;
	DelayUs(1000);
	LED_Port = 0xff;
	DelayUs(1000);
}


