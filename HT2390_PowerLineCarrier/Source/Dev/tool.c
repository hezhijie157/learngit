#include "config.h"

/*******************************************************************************************************
** 函数: ToolCalcXor8, 8位异或校验
********************************************************************************************************/
unsigned char ToolCalcXor8( unsigned char *Xor_buf, unsigned int count )
{
	u8 Xor=0x00;
	while (count--) 
	{
		Xor ^= *Xor_buf++; //Xor = Xor ^*Xor_buf;
	}
	return(Xor);
}

/*******************************************************************************************************
** 函数: ToolCalcSum, 8位和校验，不考虑溢出
********************************************************************************************************/
unsigned char ToolCalcSum( unsigned char *Sum_buf, unsigned int count )
{
	u8 Sum=0x00;
	while (count--) 
	{
		Sum += *Sum_buf++; 
	}
	return(Sum);
}





